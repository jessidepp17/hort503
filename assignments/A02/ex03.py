# printing an object not a string; the answer is the object
print(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6)

print("Is it true that 3 + 2 < 5 - 7?")

# printing True or False for the answer
print(3 + 2 < 5 - 7)

